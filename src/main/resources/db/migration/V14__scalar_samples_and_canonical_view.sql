CREATE TABLE scalar_samples
(
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    source_instance_id  INTEGER          NOT NULL REFERENCES source_instances (id),
    ingestion_record_id INTEGER REFERENCES ingestion_records (id),
    provider_record_id  TEXT,
    measured_at         TIMESTAMPTZ      NOT NULL,
    metric_type         TEXT             NOT NULL REFERENCES metric_catalog (metric_type),
    value               DOUBLE PRECISION NOT NULL,
    unit                TEXT             NOT NULL,
    context             TEXT,
    segment             TEXT,
    created_at          TIMESTAMPTZ      NOT NULL
);

CREATE UNIQUE INDEX scalar_samples_provider_record_uq
    ON scalar_samples (source_instance_id, provider_record_id, metric_type, COALESCE(segment, ''))
    WHERE provider_record_id IS NOT NULL;

CREATE INDEX scalar_samples_metric_measured_idx
    ON scalar_samples (metric_type, measured_at, id);

CREATE INDEX scalar_samples_source_metric_measured_idx
    ON scalar_samples (source_instance_id, metric_type, measured_at);

