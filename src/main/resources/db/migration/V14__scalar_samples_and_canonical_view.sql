-- Collapse the six point-in-time scalar metric tables (heart_rate_samples,
-- respiratory_rate_samples, hrv_samples, body_measurements, cardiovascular_measurements,
-- extended_body_measurements) into one generic scalar_samples table, and replace their
-- eagerly materialized canonical tables with a read-time view. The Kotlin
-- ScalarMetricRegistry is the source of truth for metric_catalog and provider_ranks;
-- this seed only has to be complete enough for the backfill below.

CREATE TABLE metric_catalog
(
    metric_type      TEXT PRIMARY KEY,
    family           TEXT             NOT NULL,
    unit             TEXT             NOT NULL,
    min_value        DOUBLE PRECISION,
    max_value        DOUBLE PRECISION,
    supports_segment BOOLEAN          NOT NULL DEFAULT FALSE
);

CREATE TABLE provider_ranks
(
    family        TEXT    NOT NULL,
    provider_code TEXT    NOT NULL,
    rank          INTEGER NOT NULL,
    PRIMARY KEY (family, provider_code)
);

